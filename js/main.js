const container = document.querySelector(".container");
function createFlower() {
    const flower = document.createElement("div");
    flower.classList.add("flower");

    // Create inner elements for the flower
    const mid = document.createElement("div");
    mid.classList.add("mid");
    flower.appendChild(mid);

    // Create petals using the reusable function
    createPetals(flower, "Petal1", 4);
    createPetals(flower, "Petal2", 4);
    createPetals(flower, "Petal3", 4);

    flower.style.left = `${getRandomNumber(-10, 80)}vw`;
    container.appendChild(flower);
}
function createPetals(flower, className, count) {
    for (let i = 1; i <= count; i++) {
        const petal = document.createElement("div");
        petal.classList.add(className, `p${i}`);
        flower.appendChild(petal);
    }
}
function getRandomNumber(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}
function createRandomFlowers() {
    const numFlowers = getRandomNumber(2, 10);
    for (let i = 0; i < numFlowers; i++) {
        createFlower();
    }
}
function removeAllFlowers() {
    const flowers = document.querySelectorAll(".flower");
    for (const flower of flowers) {
        flower.parentNode.removeChild(flower);
    }
}
function removeHalfFlowers() {
    const container = document.querySelector(".container");
    const flowers = container.querySelectorAll(".flower");
    const numFlowersToRemove = Math.floor(flowers.length / 2);

    // Eliminar la mitad de las flores
    for (let i = 0; i < numFlowersToRemove; i++) {
        flowers[i].parentNode.removeChild(flowers[i]);
    }
}

